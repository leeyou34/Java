package ch02_2_primitive;

public class StringExample {//StringExample이라는 문자열예제 클래스를 선언한다. 이 클래스는 ch-2_2_primitive 패키지 하에 있다.

	public static void main(String[] args) { // 고정적인 빈 문자열 형식의 메인 메소드를 선언한다.
		String name = "홍길동"; // 문자열 변수는 name이라고 선언하며, 그 name은 "홍길동"이라는 값이다.
		String job = "프로그래머";// 문자열 변수는 job이라고 선언하며, 그 job은 "프로그래머"라는 값이다.

		System.out.println(name); // 새로운행에 name이라는 변수를 출력해라
		System.out.println(job); //새로운행에 name이라는 변수를 출력해라.
		
	}

}
